public class Ass76 {
    public static void main(String[] args) {

        int i;
        for (i = 1; i <= 4; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("X");
            }

            System.out.println();
        }
        for (int k = 3; k >= 1; k--) {
            for (int l = 1; l <= k; l++) {
                System.out.print("X");
            }
            System.out.println();
        }

    }

}
