//A pond is 50 m long, 30 m wide and 2 m deep. Find the capacity of the pond in cubic metre.
public class Ass22 {
    public static void main(String[] args) {
        int length = 50, wide = 30, deep = 2, capacity;
        capacity = length * wide * deep;
        System.out.println("The capacity of the pond in cubic metre. is " + capacity);
    }

}
